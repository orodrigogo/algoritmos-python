# Por padrão, o input captura o valor digitado pelo usuário como texto.
valor = input('Você gosta de coca? ')
print(f'O Número {valor} é do tipo primitivo {type(valor)}.')

# Para INTEIRO
valor = int(input('Digite um número inteiro: '))
print(f'O Número {valor} é do tipo primitivo {type(valor)}.')

# Para FLOAT
valor = float(input('Digite um número float: '))
print(f'O Número {valor} é do tipo primitivo {type(valor)}.')


