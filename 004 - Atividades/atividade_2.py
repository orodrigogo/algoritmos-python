# Captura o dia, mês e ano de nascimento do usuário separado.
dia = input("Que dia você nasceu: ")
mes = input("Que mês você nasceu: ")
ano = input("Que ano você nasceu: ")

# Concatena a data de nascimento do usuário em uma frase e armazena na variavel nascimento.
nascimento = f"Você nasceu no dia {dia} de {mes} de {ano}. Correto?"

# Escreve na tela o conteúdo da variável nascimento.
print(nascimento)
