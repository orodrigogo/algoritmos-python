"""
    Crie um algoritmo que leia um número e 
    mostre o seu dobro e seu triplo.
"""

# Exemplo 1
numero = int(input("Digite o número: "))
print(f'Resultado do dobro: {numero * 2}') 
print(f'Resultado do triplo: {numero * 3}')


# Exemplo 2
dobro = (numero * 2)
triplo = (numero * 3)
print(f'O dobro do {numero} é {dobro} e o triplo do {numero} é {triplo}')






