# Pergunta o nome da disciplina
disciplina = input("Qual é a disciplina: ")

# Coleto a primeira nota.
nota1 = float(input("Digite a primeira nota: "))

# Coleto a segunda nota.
nota2 = float(input("Digite a segunda nota: "))

# Calcula a média
media = (nota1 + nota2) / 2

# Escrever na tela a média
print(f"A média da disciplina {disciplina} é {media:.2}")



